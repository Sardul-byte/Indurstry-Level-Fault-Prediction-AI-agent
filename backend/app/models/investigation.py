"""ORM model for the Investigation table.

Stores the lifecycle of a single incident investigation, including its
current status, workflow stage, raw inputs, and any error details.
"""

from __future__ import annotations

import enum
from datetime import datetime

try:
    from sqlalchemy.dialects.postgresql import JSON
except ImportError:  # pragma: no cover
    from sqlalchemy import JSON  # type: ignore[assignment]

from sqlalchemy import DateTime, String, Text, func
from sqlalchemy.orm import Mapped, mapped_column

from app.models.base import Base


class InvestigationStatus(str, enum.Enum):
    pending = "pending"
    running = "running"
    completed = "completed"
    failed = "failed"


class WorkflowStage(str, enum.Enum):
    triaging = "triaging"
    root_cause_analysis = "root_cause_analysis"
    remediation = "remediation"
    verification = "verification"
    closed = "closed"


class Investigation(Base):
    """Persists every incident investigation submitted to the system."""

    __tablename__ = "investigations"

    # Primary key — UUID generated by the application layer
    id: Mapped[str] = mapped_column(String, primary_key=True)

    # Lifecycle state
    status: Mapped[InvestigationStatus] = mapped_column(
        String,
        nullable=False,
        default=InvestigationStatus.pending,
    )
    stage: Mapped[WorkflowStage] = mapped_column(
        String,
        nullable=False,
        default=WorkflowStage.triaging,
    )

    # Raw incident inputs
    alert_payload: Mapped[dict] = mapped_column(JSON, nullable=False)
    log_data: Mapped[str] = mapped_column(Text, nullable=False)
    metadata_: Mapped[dict] = mapped_column("metadata", JSON, nullable=False)

    # Timestamps — set server-side so they are consistent across replicas
    created_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True),
        nullable=False,
        server_default=func.now(),
    )
    updated_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True),
        nullable=False,
        server_default=func.now(),
        onupdate=func.now(),
    )

    # HTTP correlation ID injected by middleware
    request_id: Mapped[str] = mapped_column(String, nullable=False)

    # Structured failure info: {agent_name, error_type, message}
    error: Mapped[dict | None] = mapped_column(JSON, nullable=True)

    def __repr__(self) -> str:  # pragma: no cover
        return (
            f"<Investigation id={self.id!r} status={self.status!r} stage={self.stage!r}>"
        )
